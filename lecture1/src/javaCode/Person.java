package javaCode;

public class Person {
    private final String name;
    private final int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public static boolean isNotDigit(char c) {
        return c < '0' || c > '9';
    }
}